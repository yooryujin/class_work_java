package variable;

public class Var2 {

    // 코드의 진입점 - main 함수
    public static void main(String[] args) {
        // 변수를 선언했다. --> 이 변수의 이름은 a 라고 지칭한다.
        int a;
        // 값을 넣다.(값을 초기화 했다.)
        a = 10;

        // 변수 b를 선언하고 동시에 값 20을 넣다.(초기화 했다.)
        // 변수 선언과 동시에 초기화
        int b = 50;
        // 변수를 사용하는 방법 - 변수에 이름을 적기만 하면 된다.
        System.out.println(b);
        System.out.println(b);
        System.out.println(b);


    } // end of main

} // end of class
