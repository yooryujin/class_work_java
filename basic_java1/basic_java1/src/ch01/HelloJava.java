package ch01;

// 탭 간격을 일정하게 유지하면 코드가 더 읽기 쉬워집니다.(가독성 권장 사항)
public class HelloJava {

    // 한 줄 주석 : 자바가 실행하지 않는 부분입니다.
    /*
       여러 줄 주석 : 긴 설명을 작성할 때 사용합니다.
     */

    // 코드의 진입점
    public static void main(String[] args) {
        // System.out.println() -> 콘솔에 괄호() 안의 내용을 출력하는 명령어 입니다.
        // 세미콜론( ; ) 문장의 끝을 표시하는 역할을 합니다.
        System.out.println("hello java");
        System.out.println("안녕 코드 수정 했어!");

    } // end of main

    // 실행 단축키 shift + F10

} // end of class
