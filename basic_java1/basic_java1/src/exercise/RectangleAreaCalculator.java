package exercise;

public class RectangleAreaCalculator {

    public static void main(String[] args) {

        // 단, 상수를 사용해 보세요
        // 직접 코드의 흐름을 만들어 보세요

        // 주석으로 작성 습관
        // 사각형의 가로 길이를 상수로 선언해보자
        final int A = 20;
        // 사각형의 세로 길이를 상수로 선언해보자
        final int B = 6;
        // 사각형의 면적 계산 식을 만들고 그 연산의 결과 값을 area 변수에 담아보자.
        int area = A * B;

        System.out.println("사각형의 면적 : " + area);


    } // end of main

} // end of class
